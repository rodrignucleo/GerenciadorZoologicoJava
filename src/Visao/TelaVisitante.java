    public void preencherTabelaAnimal(String SQL){
        ArrayList dados = new ArrayList();
        
        String [] Colunas = new String[]{"ID", "NÂº JAULA", "ANIMAL", "SEXO", "VETERINARIO RESPONSAVEL"};
        
        conectar.executaSQL(SQL);
       
        try {
            conectar.rs.first();
            do{
                dados.add(new Object[]{conectar.rs.getLong("ID"), conectar.rs.getString("IDJAULA"), conectar.rs.getString("NOMEANIMAL"), conectar.rs.getString("SEXO"), conectar.rs.getString("VETERINARIORESPONSAVEL")});
            }while(conectar.rs.next());
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao preencher o ArrayList! \n Erro: " + ex.getMessage());
        }
            AnimalModeloTabela adicionaranimal = new AnimalModeloTabela(dados, Colunas);
            
            TabelaAnimal.setModel(adicionaranimal);
            TabelaAnimal.getColumnModel().getColumn(0).setPreferredWidth(50);
            TabelaAnimal.getColumnModel().getColumn(0).setResizable(false);
            TabelaAnimal.getColumnModel().getColumn(1).setPreferredWidth(160);
            TabelaAnimal.getColumnModel().getColumn(1).setResizable(false);
            TabelaAnimal.getColumnModel().getColumn(2).setPreferredWidth(150);
            TabelaAnimal.getColumnModel().getColumn(2).setResizable(false);
            TabelaAnimal.getColumnModel().getColumn(3).setPreferredWidth(100);
            TabelaAnimal.getColumnModel().getColumn(3).setResizable(false);
            TabelaAnimal.getColumnModel().getColumn(4).setPreferredWidth(175);
            TabelaAnimal.getColumnModel().getColumn(4).setResizable(false);
            
            TabelaAnimal.getTableHeader().setReorderingAllowed(false);
            TabelaAnimal.setAutoResizeMode(TabelaJaula.AUTO_RESIZE_OFF);
            TabelaAnimal.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

    }
    
    public void preencherTabelaJaula(String SQL){
        ArrayList dados = new ArrayList();
        
        String [] Colunas = new String[]{"ID", "LOCALIZACAO"};
        

        conectar.executaSQL(SQL);
       
        try {
            conectar.rs.first();
            do{
                dados.add(new Object[]{conectar.rs.getLong("ID"), conectar.rs.getString("LOCALIZACAO")});
            }while(conectar.rs.next());
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao preencher o ArrayList! \n Erro: " + ex.getMessage());
        }
            JaulaModeloTabela adicionarjaula = new JaulaModeloTabela(dados, Colunas);
            
            TabelaJaula.setModel(adicionarjaula);
            TabelaJaula.getColumnModel().getColumn(0).setPreferredWidth(50);
            TabelaJaula.getColumnModel().getColumn(0).setResizable(false);
            TabelaJaula.getColumnModel().getColumn(1).setPreferredWidth(120);
            TabelaJaula.getColumnModel().getColumn(1).setResizable(false);
            
            TabelaJaula.getTableHeader().setReorderingAllowed(false);
            TabelaJaula.setAutoResizeMode(TabelaJaula.AUTO_RESIZE_OFF);
            TabelaJaula.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }
    
